SELECT
    ESAME.CodE AS id_visita,
    Paziente.nome,
    Paziente.cognome,
    Paziente.sesso,
    YEAR(CURRENT_DATE) - YEAR(Paziente.Data_Nascita) AS età,
    CASE 
       
        WHEN YEAR(CURRENT_DATE) - YEAR(Paziente.Data_Nascita) BETWEEN 0 AND 30 THEN 'Giovane' 
        WHEN YEAR(CURRENT_DATE) - YEAR(Paziente.Data_Nascita) BETWEEN 31 AND 60 THEN 'Adulto' 
        ELSE 'Anziano' 
    END AS fasciaEtà,
    Sede_Ospedaliera.Nome AS Nome_Sede,
    Data_esame.giorno,
    Data_esame.mese,
    Data_esame.orario,
    
    Farmaco.Nome_farmaco AS Nome_Farmaco,
    Farmaco.Prezzo AS Prezzo_Farmaco

FROM
    ESAME
    JOIN Data_esame ON ESAME.Esame_data = Data_esame.CodData
    JOIN Paziente ON ESAME.Paziente_esame = Paziente.CodF
    JOIN Sede_Ospedaliera ON ESAME.Sede_esame = Sede_Ospedaliera.CodAZ
    JOIN Farmaco ON ESAME.Prescrizione_farmaco = Farmaco.CodFA;

